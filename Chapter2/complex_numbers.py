#!/usr/bin/python
"""
    Purpose: complex Numbers
                real+imaginary
"""
num1 =10
print(type(num1))
num1 = 10+1j
print(num1)
print(type(num1))
print("num1.real",num1.real)
print("num1.imag",num1.imag)
print("num1.conjugate",num1.conjugate())
print("type(num1.conjugate())",type(num1.conjugate()))